package com.feng.chuangyierp.model.vo.hr;


public class SalaryChangeLog {

  private String gh;
  private double tzz;
  private java.sql.Timestamp tzsj;
  private String tzyy;


  public String getGh() {
    return gh;
  }

  public void setGh(String gh) {
    this.gh = gh;
  }


  public double getTzz() {
    return tzz;
  }

  public void setTzz(double tzz) {
    this.tzz = tzz;
  }


  public java.sql.Timestamp getTzsj() {
    return tzsj;
  }

  public void setTzsj(java.sql.Timestamp tzsj) {
    this.tzsj = tzsj;
  }


  public String getTzyy() {
    return tzyy;
  }

  public void setTzyy(String tzyy) {
    this.tzyy = tzyy;
  }

}
